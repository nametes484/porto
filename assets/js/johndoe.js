function downloadResume() {
  // Replace 'YOUR_GOOGLE_DRIVE_LINK' with your actual Google Drive link
  var driveLink = 'https://drive.google.com/file/d/11b3NwxwUMp_fIgXn6VqMVzcvcilFEdaQ/view?usp=drive_link';

  // Get the file ID from the Google Drive link
  var fileId = driveLink.match(/\/d\/(.*?)(?:[?/]|$)/)[1];

  // Construct a direct download link
  var downloadLink = 'https://drive.google.com/uc?export=download&id=' + fileId;

  // Create an invisible link element
  var link = document.createElement('a');
  link.href = downloadLink;
  link.download = 'resume.pdf';

  // Append the link to the document and trigger a click event
  document.body.appendChild(link);
  link.click();

  // Remove the link from the document
  document.body.removeChild(link);
}
